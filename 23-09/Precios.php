<h3>Trámite de envío:</h3>

<p>Usted puede elegir entre los siguientes métodos de envío:</p>

<p><ul><li>Envío por correo tradicional (contra-reembolso).</li></ul>
<ul><li>Envío por correo urgente (contra-reembolso).</li></ul>
<ul><li>Alojamiento de archivos en el servidor que usted desée (Ejemplo: MediaFire, Rapidshare, etc...).</li></ul></p>

<p>* Envío contra-reembolso a toda <strong>España</strong> y al <strong>extranjero</strong>.</p>

<p>* El alojamiento de archivos requiere un gran esfuerzo además de consumo de tiempo (Partir el contenido en trozos, comprimirlos, muchas horas para subirlos, volverlos a subir si eliminan el enlace, etc...), yo no dispongo de un buen ancho de banda, por eso este servicio tendrá un coste adicional de 10 € por cada DVD subido al servidor.</p>
